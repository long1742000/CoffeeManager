    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        // tạo thanh search tự động
        DefaultTableModel model = (DefaultTableModel)tbl_account.getModel();
        String search = txt_search.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);        // tạo 1 cái biến sắp xếp lại bảng theo txt search
        tbl_account.setRowSorter(tr);                                                                           
        tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_txt_searchKeyReleased

    private void tbl_accountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_accountMouseClicked
        // nhấp vào bảng
        int row = tbl_account.getSelectedRow();
        TableModel table = tbl_account.getModel();
        int id = Integer.parseInt(String.valueOf(table.getValueAt(row, 0)));
        AccountDetailForm ad = new AccountDetailForm(id);
        ad.setVisible(true);
        ad.setLocationRelativeTo(null);
    }//GEN-LAST:event_tbl_accountMouseClicked


    public void loadData(){    
        String status;
        DefaultTableModel model = (DefaultTableModel)tbl_account.getModel();
        EmployeeDAO empDAO = new EmployeeDAO();
        ArrayList<Employee> list = empDAO.getAllStaff();
        int count = model.getRowCount();
        while(count>0){
            model.removeRow(count-1);
            count--;
        }
        for(Employee item:list){
            if(item.isStatus()){                                                // nếu account nhân viên này chưa huỷ => còn làm
                status="Still working";
            }
            else{
                status="Has retired";                                           // nếu huỷ r thì nó nghỉ làm r đó :V
            }
            Object[] row = {item.getID(), item.getFullName(), item.getPhoneNumber(), status};
            model.addRow(row);
        }
    }


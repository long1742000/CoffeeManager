    public void loadData(){                                                             // Hàm tải dữ liệu bảng Product lên (lấy tất cả)
        DefaultTableModel model = (DefaultTableModel)tbl_exchange.getModel();
        ProductDAO proDAO = new ProductDAO();
        ArrayList<Product> list = proDAO.getAllDish();
        int count = model.getRowCount();
        while(count>0){
            model.removeRow(count-1);
            count--;
        }
        for(Product item:list){
            Object[] row = {item.getID(), item.getDish().getName(), item.getSize().getName(), item.getPoints()};
            model.addRow(row);
        }
    }
    

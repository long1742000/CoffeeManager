    private void tbl_exchangeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_exchangeMouseClicked
        // TODO add your handling code here:
        int row = tbl_exchange.getSelectedRow();                                                // Chọn sản phẩm để đổi
        double point = Double.parseDouble(String.valueOf(tbl_exchange.getValueAt(row, 3)));     // điểm của sản phẩm
        double pointCus = Double.parseDouble(txt_points.getText());                             // điểm của thằng khách
        if(pointCus<point){                                                                     // nếu như điểm thằng khách < điểm sản phẩm muốn đổi
            JOptionPane.showMessageDialog(this, "Member's point lower than product's point");   // kêu nó chọn cái nào đổi được
            but_enter.setEnabled(false);                // Không đủ điểm đổi thì khoá nút Enter lại
        }
        else{
            but_enter.setEnabled(true);                 // nếu đủ thì oke mở ra cho nó 
        }
    }//GEN-LAST:event_tbl_exchangeMouseClicked

    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        // thanh Search
        DefaultTableModel model = (DefaultTableModel)tbl_exchange.getModel();
        String search = txt_search.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);        // tạo 1 cái biến sắp xếp lại bảng theo txt search
        tbl_exchange.setRowSorter(tr);                                                                           
        tr.setRowFilter(RowFilter.regexFilter(search));                                         // sắp xếp lại theo dữ liệu search 
    }//GEN-LAST:event_txt_searchKeyReleased

    private void but_enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_but_enterActionPerformed

        CustomerDAO cusDAO = new CustomerDAO();
        int row = tbl_exchange.getSelectedRow();                                                // lấy dòng đang chọn
        double point = Double.parseDouble(String.valueOf(tbl_exchange.getValueAt(row, 3)));     // lấy points của sản phẩm
        double pointCus = Double.parseDouble(txt_points.getText());                             // lấy points của khách
        double pointEx = pointCus - point;                              // bắt đầu trừ để lấy số dư points còn lại của khách
        
        int idEx = Integer.parseInt(String.valueOf(tbl_exchange.getValueAt(row, 0)));           // lúc này lấy cái idEx từ đầu chương trình ra sử dụng, lấy cái ID của sản phẩm muốn Exchange gán cho nó
        int idEm = Integer.parseInt(lbl_user.getText());                        // lấy id thằng nhân viên (đang bán)
        int idCus = Integer.parseInt(lbl_cus.getText());                        // lấy id thằng khách
        int totalAllDay = Integer.parseInt(lbl_totalAllDay.getText());
        int cash = Integer.parseInt(lbl_cash.getText());
        int momo = Integer.parseInt(lbl_momo.getText());
        int vnpay = Integer.parseInt(lbl_vnpay.getText());
        int discount = Integer.parseInt(lbl_discount.getText());
        int type;
        cusDAO.addPoints(pointEx, idCus);                               // sau khi đổi xong thì update điểm thằng khách theo số dư còn lại
        
        
        MemberForm m = new MemberForm(idEm, idCus, totalAllDay, cash, momo, vnpay, discount, idEx);
        m.setLocationRelativeTo(null);
        m.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_but_enterActionPerformed

